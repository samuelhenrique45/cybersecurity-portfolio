class Vulnerability:
    """Estrutura para armazenar vulnerabilidade encontrada"""
    
    SEVERITIES = {
        "CRITICAL": 4,
        "HIGH": 3,
        "MEDIUM": 2,
        "LOW": 1,
        "INFO": 0
    }
    
    def __init__(self, name: str, url: str, severity: str, description: str, 
                 payload: str = None, evidence: str = None, remediation: str = None):
        self.name = name
        self.url = url
        self.severity = severity
        self.description = description
        self.payload = payload
        self.evidence = evidence
        self.remediation = remediation
        self.timestamp = datetime.now()
    
    def to_dict(self) -> dict:
        return {
            "name": self.name,
            "url": self.url,
            "severity": self.severity,
            "description": self.description,
            "payload": self.payload,
            "evidence": self.evidence,
            "remediation": self.remediation,
            "timestamp": self.timestamp.isoformat()
        }
    
    def __repr__(self):
        return f"[{self.severity}] {self.name} @ {self.url}"


class WebScanner:
    """Scanner de vulnerabilidades web"""
    
    def __init__(self, target_url: str, use_threading: bool = True):
        self.target_url = target_url.rstrip('/')
        self.use_threading = use_threading
        self.session = self._create_session()
        self.vulnerabilities: List[Vulnerability] = []
        self.discovered_urls: Set[str] = set()
        self.forms: List[Dict] = []
        self.lock = threading.Lock()
        self.scan_start = datetime.now()
        
        # Estatísticas
        self.requests_made = 0
        self.requests_succeeded = 0
        self.requests_failed = 0
    
    def _create_session(self) -> requests.Session:
        """Cria sessão HTTP com configurações padrão"""
        session = requests.Session()
        session.headers.update({
            'User-Agent': Config.USER_AGENT,
            'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
            'Accept-Language': 'en-US,en;q=0.5',
            'Connection': 'keep-alive',
        })
        return session
    
    def _make_request(self, url: str, params: dict = None, data: dict = None,
                      method: str = 'GET') -> Optional[requests.Response]:
        """Faz requisição HTTP com tratamento de erros"""
        try:
            with self.lock:
                self.requests_made += 1
            
            if method.upper() == 'GET':
                resp = self.session.get(url, params=params, timeout=Config.TIMEOUT,
                                        allow_redirects=False)
            else:
                resp = self.session.post(url, data=data, timeout=Config.TIMEOUT,
                                         allow_redirects=False)
            
            with self.lock:
                self.requests_succeeded += 1
            
            time.sleep(Config.DELAY_BETWEEN_REQUESTS)
            return resp
        
        except requests.exceptions.Timeout:
            with self.lock:
                self.requests_failed += 1
            return None
        except Exception as e:
            with self.lock:
                self.requests_failed += 1
            return None
    
    def _extract_forms(self, url: str) -> List[Dict]:
        """Extrai todos os formulários HTML de uma página"""
        response = self._make_request(url)
        if not response:
            return []
        
        soup = BeautifulSoup(response.text, 'html.parser')
        forms = []
        
        for form in soup.find_all('form'):
            form_data = {
                'action': urljoin(url, form.get('action', '')),
                'method': form.get('method', 'GET').upper(),
                'inputs': []
            }
            
            for input_tag in form.find_all(['input', 'textarea', 'select']):
                input_info = {
                    'name': input_tag.get('name', ''),
                    'type': input_tag.get('type', 'text'),
                    'value': input_tag.get('value', '')
                }
                if input_info['name']:
                    form_data['inputs'].append(input_info)
            
            forms.append(form_data)
        
        return forms
    
    def _test_sql_injection(self, url: str, params: dict = None, data: dict = None) -> List[Vulnerability]:
        """Testa SQL Injection em parâmetros"""
        vulns = []
        
        # Decide qual parâmetro testar
        test_params = params or data or {}
        
        for param_name, original_value in test_params.items():
            for payload in Config.SQL_PAYLOADS:
                if params:
                    test_params_copy = params.copy()
                    test_params_copy[param_name] = payload
                    response = self._make_request(url, params=test_params_copy)
                else:
                    test_data_copy = data.copy() if data else {}
                    test_data_copy[param_name] = payload
                    response = self._make_request(url, data=test_data_copy, method='POST')
                
                if response:
                    # Verificar padrões de erro SQL
                    for pattern in Config.SQL_ERROR_PATTERNS:
                        if re.search(pattern, response.text, re.IGNORECASE):
                            vuln = Vulnerability(
                                name="SQL Injection",
                                url=url,
                                severity="HIGH",
                                description=f"Parâmetro '{param_name}' parece vulnerável a SQL Injection",
                                payload=payload,
                                evidence=response.text[:500],
                                remediation="Use prepared statements/parameterized queries, escape inputs, use ORM"
                            )
                            vulns.append(vuln)
                            break
                    
                    # Verificar time-based injection
                    if "SLEEP" in payload or "WAITFOR" in payload:
                        elapsed = response.elapsed.total_seconds()
                        if elapsed >= 4:
                            vuln = Vulnerability(
                                name="SQL Injection (Time-based)",
                                url=url,
                                severity="HIGH",
                                description=f"Parâmetro '{param_name}' mostra time-based SQL injection",
                                payload=payload,
                                evidence=f"Response time: {elapsed:.2f}s",
                                remediation="Use prepared statements and avoid dynamic query building"
                            )
                            vulns.append(vuln)
        
        return vulns
    
    def _test_xss(self, url: str, params: dict = None, data: dict = None) -> List[Vulnerability]:
        """Testa XSS em parâmetros"""
        vulns = []
        
        test_params = params or data or {}
        
        for param_name, original_value in test_params.items():
            for payload in Config.XSS_PAYLOADS:
                if params:
                    test_params_copy = params.copy()
                    test_params_copy[param_name] = payload
                    response = self._make_request(url, params=test_params_copy)
                else:
                    test_data_copy = data.copy() if data else {}
                    test_data_copy[param_name] = payload
                    response = self._make_request(url, data=test_data_copy, method='POST')
                
                if response and payload in response.text:
                    # Verificar se realmente foi renderizado (não escapado)
                    if payload in response.text and not html.escape(payload) in response.text:
                        vuln = Vulnerability(
                            name="Cross-Site Scripting (XSS)",
                            url=url,
                            severity="HIGH",
                            description=f"Parâmetro '{param_name}' parece vulnerável a XSS",
                            payload=payload,
                            evidence=response.text[:500],
                            remediation="Use escape/encode output, Content-Security-Policy, sanitize inputs"
                        )
                        vulns.append(vuln)
                        break
        
        return vulns
    
    def _test_open_redirect(self, url: str, params: dict = None) -> List[Vulnerability]:
        """Testa Open Redirect em parâmetros de URL"""
        vulns = []
        
        if not params:
            return vulns
        
        for param_name, original_value in params.items():
            for payload in Config.REDIRECT_PAYLOADS:
                test_params = params.copy()
                test_params[param_name] = payload
                response = self._make_request(url, params=test_params)
                
                if response and response.status_code in [301, 302, 303, 307, 308]:
                    location = response.headers.get('Location', '')
                    if 'evil.com' in location or '//evil' in location:
                        vuln = Vulnerability(
                            name="Open Redirect",
                            url=url,
                            severity="MEDIUM",
                            description=f"Parâmetro '{param_name}' permite redirecionamento não validado",
                            payload=payload,
                            evidence=f"Redirect to: {location}",
                            remediation="Validate redirect URLs against whitelist, avoid using user input directly"
                        )
                        vulns.append(vuln)
        
        return vulns
    
    def _brute_force_directories(self) -> List[Vulnerability]:
        """Força brute de diretórios para encontrar recursos sensíveis"""
        vulns = []
        found_dirs = []
        
        for directory in Config.DIRECTORIES:
            test_url = urljoin(self.target_url + '/', directory)
            response = self._make_request(test_url)
            
            if response and response.status_code == 200:
                found_dirs.append(test_url)
                
                # Classificar severidade baseado no recurso
                severity = "MEDIUM"
                if any(sensitive in directory.lower() for sensitive in ['admin', 'config', 'sql', 'backup', '.git', '.env']):
                    severity = "HIGH"
                elif any(info in directory.lower() for info in ['log', 'debug', 'test', 'dump']):
                    severity = "MEDIUM"
                else:
                    severity = "LOW"
                
                vuln = Vulnerability(
                    name="Sensitive Directory/File Exposure",
                    url=test_url,
                    severity=severity,
                    description=f"Recurso sensível encontrado: {directory}",
                    payload=directory,
                    evidence=f"HTTP {response.status_code} - Content-Length: {len(response.text)}",
                    remediation="Remove sensitive files from web root, use authentication, configure proper access controls"
                )
                vulns.append(vuln)
        
        return vulns
    
    def _analyze_headers(self, url: str) -> List[Vulnerability]:
        """Analisa cabeçalhos HTTP em busca de configurações inseguras"""
        vulns = []
        response = self._make_request(url)
        
        if not response:
            return vulns
        
        headers = response.headers
        
        # Verificar HSTS
        if 'Strict-Transport-Security' not in headers:
            vuln = Vulnerability(
                name="Missing HSTS Header",
                url=url,
                severity="MEDIUM",
                description="HTTP Strict Transport Security (HSTS) não está implementado",
                remediation="Add 'Strict-Transport-Security: max-age=31536000; includeSubDomains' header"
            )
            vulns.append(vuln)
        
        # Verificar X-Frame-Options
        if 'X-Frame-Options' not in headers:
            vuln = Vulnerability(
                name="Missing X-Frame-Options",
                url=url,
                severity="MEDIUM",
                description="Site vulnerável a clickjacking",
                remediation="Add 'X-Frame-Options: DENY' or 'SAMEORIGIN' header"
            )
            vulns.append(vuln)
        
        # Verificar X-Content-Type-Options
        if 'X-Content-Type-Options' not in headers:
            vuln = Vulnerability(
                name="Missing X-Content-Type-Options",
                url=url,
                severity="LOW",
                description="Possível MIME type sniffing",
                remediation="Add 'X-Content-Type-Options: nosniff' header"
            )
            vulns.append(vuln)
        
        # Verificar CSP
        if 'Content-Security-Policy' not in headers:
            vuln = Vulnerability(
                name="Missing CSP Header",
                url=url,
                severity="MEDIUM",
                description="Content Security Policy não configurada",
                remediation="Implement CSP to mitigate XSS and data injection attacks"
            )
            vulns.append(vuln)
        
        # Verificar server info leaking
        if 'Server' in headers:
            vuln = Vulnerability(
                name="Server Information Leakage",
                url=url,
                severity="LOW",
                description=f"Server version exposta: {headers['Server']}",
                evidence=headers['Server'],
                remediation="Remove or obfuscate Server header"
            )
            vulns.append(vuln)
        
        return vulns
    
    def _check_for_errors_info_leak(self, url: str, response_text: str) -> List[Vulnerability]:
        """Verifica se há vazamento de informações em mensagens de erro"""
        vulns = []
        
        error_patterns = [
            (r"Fatal error:", "HIGH", "PHP Fatal Error com informações de sistema"),
            (r"Warning:", "MEDIUM", "PHP Warning com caminhos de sistema"),
            (r"Notice:", "LOW", "PHP Notice"),
            (r"Stack trace:", "HIGH", "Stack trace exposto"),
            (r"Exception in", "MEDIUM", "Exception details expostas"),
            (r"/home/", "MEDIUM", "Caminho do sistema exposto"),
            (r"/var/www/", "MEDIUM", "Caminho do webroot exposto"),
            (r"root@", "HIGH", "Credencial ou username exposto"),
            (r"password", "CRITICAL", "Palavra 'password' no response")
        ]
        
        for pattern, severity, description in error_patterns:
            if re.search(pattern, response_text, re.IGNORECASE):
                vuln = Vulnerability(
                    name="Information Disclosure",
                    url=url,
                    severity=severity,
                    description=description,
                    evidence=response_text[:500],
                    remediation="Configure error reporting to hide sensitive information"
                )
                vulns.append(vuln)
        
        return vulns
    
    def scan_forms(self) -> List[Vulnerability]:
        """Escaneia formulários em busca de vulnerabilidades"""
        vulns = []
        forms = self._extract_forms(self.target_url)
        
        print(f"[+] Encontrados {len(forms)} formulários")
        
        for form in forms:
            form_url = form['action']
            print(f"    Escaneando formulário: {form_url}")
            
            # Preparar dados padrão
            test_data = {}
            for input_info in form['inputs']:
                if input_info['type'] not in ['submit', 'button', 'reset']:
                    test_data[input_info['name']] = 'test'
            
            # Testar SQL Injection
            sql_vulns = self._test_sql_injection(form_url, data=test_data)
            vulns.extend(sql_vulns)
            
            # Testar XSS
            xss_vulns = self._test_xss(form_url, data=test_data)
            vulns.extend(xss_vulns)
        
        return vulns
    
