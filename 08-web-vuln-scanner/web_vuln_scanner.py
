    def run_full_scan(self) -> List[Vulnerability]:
        """Executa scan completo no alvo"""
        print("\n" + "=" * 70)
        print(f"🔍 INICIANDO SCAN: {self.target_url}")
        print("=" * 70)
        
        # 1. Scan da URL principal
        print("\n[1/5] Escaneando URL principal...")
        main_vulns = self.scan_url_params(self.target_url)
        self.vulnerabilities.extend(main_vulns)
        
        # 2. Scan de formulários
        print("\n[2/5] Escaneando formulários...")
        form_vulns = self.scan_forms()
        self.vulnerabilities.extend(form_vulns)
        
        # 3. Brute force de diretórios
        print("\n[3/5] Forçando diretórios...")
        dir_vulns = self._brute_force_directories()
        self.vulnerabilities.extend(dir_vulns)
        
        # 4. Análise de cabeçalhos
        print("\n[4/5] Analisando cabeçalhos HTTP...")
        header_vulns = self._analyze_headers(self.target_url)
        self.vulnerabilities.extend(header_vulns)
        
        # 5. Verificação de info leak
        print("\n[5/5] Verificando vazamento de informações...")
        response = self._make_request(self.target_url)
        if response:
            info_vulns = self._check_for_errors_info_leak(self.target_url, response.text)
            self.vulnerabilities.extend(info_vulns)
        
        return self.vulnerabilities


