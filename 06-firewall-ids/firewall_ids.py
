class FirewallEngine:
    """Firewall stateful com bloqueio de IPs"""
    
    def __init__(self):
        self.rules = self.load_rules()
        self.blocked_until = {}
        self.connections = defaultdict(lambda: defaultdict(dict))
        self.lock = threading.Lock()
    
    def load_rules(self) -> Dict:
        if os.path.exists(Config.RULES_FILE):
            with open(Config.RULES_FILE, 'r') as f:
                return json.load(f)
        return {
            "blocked_ips": list(Config.BLOCKED_IPS),
            "allowed_ips": list(Config.ALLOWED_IPS),
            "blocked_ports": list(Config.BLOCKED_PORTS),
            "allowed_ports": list(Config.ALLOWED_PORTS),
            "rate_limit": Config.RATE_LIMIT
        }
    
    def save_rules(self):
        with open(Config.RULES_FILE, 'w') as f:
            json.dump(self.rules, f, indent=2)
    
    def is_blocked(self, ip: str) -> Tuple[bool, str]:
        """Verifica se IP está bloqueado"""
        if ip in self.rules["allowed_ips"]:
            return False, "whitelisted"
        
        if ip in self.rules["blocked_ips"]:
            return True, "permanent_block"
        
        if ip in self.blocked_until:
            if time.time() < self.blocked_until[ip]:
                remaining = int(self.blocked_until[ip] - time.time())
                return True, f"temporary_block ({remaining}s remaining)"
            else:
                del self.blocked_until[ip]
        
        return False, "allowed"
    
    def block_ip(self, ip: str, reason: str, duration: int = Config.BLOCK_DURATION):
        """Bloqueia IP temporariamente ou permanentemente"""
        with self.lock:
            if ip in self.rules["allowed_ips"]:
                return
            
            self.blocked_until[ip] = time.time() + duration
            
            # Se severidade alta, adicionar à blacklist permanente
            if "flood" in reason.lower() or "critical" in reason.lower():
                if ip not in self.rules["blocked_ips"]:
                    self.rules["blocked_ips"].append(ip)
                    self.save_rules()
            
            self.log_action("BLOCK", ip, reason, duration)
    
    def unblock_ip(self, ip: str):
        """Desbloqueia IP"""
        with self.lock:
            if ip in self.blocked_until:
                del self.blocked_until[ip]
            if ip in self.rules["blocked_ips"]:
                self.rules["blocked_ips"].remove(ip)
                self.save_rules()
            self.log_action("UNBLOCK", ip, "Manual unblock", 0)
    
    def allow_ip(self, ip: str):
        """Adiciona IP à whitelist"""
        with self.lock:
            if ip not in self.rules["allowed_ips"]:
                self.rules["allowed_ips"].append(ip)
                if ip in self.rules["blocked_ips"]:
                    self.rules["blocked_ips"].remove(ip)
                self.save_rules()
            self.log_action("ALLOW", ip, "Added to whitelist", 0)
    
    def check_packet(self, src_ip: str, dst_port: int, protocol: str) -> Tuple[bool, str]:
        """Avalia se pacote deve ser permitido"""
        blocked, reason = self.is_blocked(src_ip)
        if blocked:
            return False, reason
        
        # Regras de porta
        if dst_port in self.rules["blocked_ports"]:
            return False, f"blocked_port_{dst_port}"
        
        # Regras de protocolo
        if protocol.upper() == "ICMP":
            return True, "icmp_allowed"
        
        return True, "allowed"
    
    def log_action(self, action: str, ip: str, reason: str, duration: int = 0):
        """Registra ação do firewall"""
        timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        log_entry = f"[{timestamp}] {action} {ip} - {reason}"
        if duration:
            log_entry += f" (duration: {duration}s)"
        
        with open(Config.LOG_FILE, 'a') as f:
            f.write(log_entry + '\n')
        
        self._print_alert(action, ip, reason)
    
    def _print_alert(self, action: str, ip: str, reason: str):
        """Print colorido no console"""
        colors = {
            "BLOCK": "\033[91m",  # Red
            "ALLOW": "\033[92m",  # Green
            "ALERT": "\033[93m",  # Yellow
            "UNBLOCK": "\033[94m",  # Blue
            "CRITICAL": "\033[95m",  # Purple
        }
        color = colors.get(action, "\033[0m")
        print(f"{color}[{action}] {ip} - {reason}\033[0m")


