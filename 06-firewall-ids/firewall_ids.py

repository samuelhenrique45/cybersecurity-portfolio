class AttackTracker:
    """Rastreia métricas por IP para detectar ataques"""
    
    def __init__(self):
        self.syn_packets = defaultdict(lambda: deque(maxlen=1000))
        self.port_hits = defaultdict(lambda: defaultdict(lambda: deque(maxlen=100)))
        self.unique_ports = defaultdict(lambda: set())
        self.auth_failures = defaultdict(lambda: deque(maxlen=100))
        self.packet_count = defaultdict(lambda: deque(maxlen=1000))
        self.last_block_time = defaultdict(float)
    
    def add_syn_packet(self, ip: str, timestamp: float):
        self.syn_packets[ip].append(timestamp)
        # Clean old entries
        while self.syn_packets[ip] and timestamp - self.syn_packets[ip][0] > 1:
            self.syn_packets[ip].popleft()
    
    def get_syn_rate(self, ip: str) -> int:
        return len(self.syn_packets[ip])
    
    def add_port_hit(self, ip: str, port: int, timestamp: float):
        if port not in self.port_hits[ip]:
            self.port_hits[ip][port] = deque(maxlen=100)
        self.port_hits[ip][port].append(timestamp)
        self.unique_ports[ip].add(port)
        
        # Clean old unique ports
        cutoff = timestamp - Config.PORT_SCAN_WINDOW
        for p in list(self.unique_ports[ip]):
            if p not in self.port_hits[ip] or all(t < cutoff for t in self.port_hits[ip][p]):
                self.unique_ports[ip].discard(p)
    
    def get_unique_ports(self, ip: str, timestamp: float) -> int:
        return len(self.unique_ports[ip])
    
    def add_auth_failure(self, ip: str, timestamp: float):
        self.auth_failures[ip].append(timestamp)
        while self.auth_failures[ip] and timestamp - self.auth_failures[ip][0] > Config.BRUTE_FORCE_WINDOW:
            self.auth_failures[ip].popleft()
    
    def get_auth_failures(self, ip: str) -> int:
        return len(self.auth_failures[ip])
    
    def add_packet(self, ip: str, timestamp: float):
        self.packet_count[ip].append(timestamp)
        while self.packet_count[ip] and timestamp - self.packet_count[ip][0] > 1:
            self.packet_count[ip].popleft()
    
    def get_packet_rate(self, ip: str) -> int:
        return len(self.packet_count[ip])


