class IDSEngine:
    """Detecta padrões de ataque em tempo real"""
    
    def __init__(self, firewall: FirewallEngine):
        self.firewall = firewall
        self.tracker = AttackTracker()
        self.alerts = []
        self.lock = threading.Lock()
        
        # Padrões de brute force em payloads
        self.brute_patterns = [
            b"Failed password",
            b"authentication failure",
            b"Invalid user",
            b"login incorrect",
            b"Password wrong",
            b"Access denied"
        ]
    
    def add_alert(self, severity: str, attack_type: str, src_ip: str, dst_ip: str,
                  details: str, packet_info: dict = None):
        """Registra alerta de segurança"""
        alert = {
            "timestamp": datetime.now().isoformat(),
            "severity": severity,  # LOW, MEDIUM, HIGH, CRITICAL
            "type": attack_type,
            "src_ip": src_ip,
            "dst_ip": dst_ip,
            "details": details,
            "packet": packet_info
        }
        
        with self.lock:
            self.alerts.append(alert)
            
            # Salvar em arquivo
            with open(Config.ALERT_LOG, 'a') as f:
                f.write(json.dumps(alert) + '\n')
            
            # Print colorido
            colors = {
                "LOW": "\033[92m",
                "MEDIUM": "\033[93m",
                "HIGH": "\033[95m",
                "CRITICAL": "\033[91m"
            }
            color = colors.get(severity, "\033[0m")
            print(f"\n{color}🚨 ALERTA [{severity}] {attack_type}")
            print(f"   De: {src_ip} | Para: {dst_ip}")
            print(f"   {details}\033[0m")
    
    def detect_syn_flood(self, packet, src_ip: str, timestamp: float):
        """Detecta SYN Flood attack"""
        if not packet.haslayer(TCP):
            return
        
        tcp = packet[TCP]
        if tcp.flags & 0x02:  # SYN flag
            self.tracker.add_syn_packet(src_ip, timestamp)
            rate = self.tracker.get_syn_rate(src_ip)
            
            if rate > Config.SYN_FLOOD_THRESHOLD:
                self.add_alert(
                    "HIGH", "SYN_FLOOD", src_ip, packet[IP].dst,
                    f"SYN rate: {rate} packets/second (threshold: {Config.SYN_FLOOD_THRESHOLD})"
                )
                self.firewall.block_ip(src_ip, f"SYN flood detected ({rate}/s)", 600)
    
    def detect_port_scan(self, packet, src_ip: str, timestamp: float):
        """Detecta Port Scanning"""
        if packet.haslayer(TCP):
            dst_port = packet[TCP].dport
        elif packet.haslayer(UDP):
            dst_port = packet[UDP].dport
        else:
            return
        
        self.tracker.add_port_hit(src_ip, dst_port, timestamp)
        unique_ports = self.tracker.get_unique_ports(src_ip, timestamp)
        
        if unique_ports >= Config.PORT_SCAN_THRESHOLD:
            self.add_alert(
                "MEDIUM", "PORT_SCAN", src_ip, packet[IP].dst,
                f"Scanned {unique_ports} unique ports in {Config.PORT_SCAN_WINDOW}s"
            )
    
    def detect_brute_force(self, packet, src_ip: str, dst_ip: str, timestamp: float):
        """Detecta Brute Force attempts"""
        if not packet.haslayer(Raw):
            return
        
        payload = packet[Raw].load
        payload_lower = payload.lower()
        
        # Verificar padrões de falha de autenticação
        for pattern in self.brute_patterns:
            if pattern in payload or pattern.lower() in payload_lower:
                self.tracker.add_auth_failure(src_ip, timestamp)
                failures = self.tracker.get_auth_failures(src_ip)
                
                if failures >= Config.BRUTE_FORCE_THRESHOLD:
                    self.add_alert(
                        "HIGH", "BRUTE_FORCE", src_ip, dst_ip,
                        f"{failures} failed authentication attempts in {Config.BRUTE_FORCE_WINDOW}s"
                    )
                    self.firewall.block_ip(src_ip, f"Brute force detected ({failures} attempts)", 900)
                break
    
    def detect_rate_limit_exceeded(self, packet, src_ip: str, timestamp: float):
        """Detecta excesso de taxa de pacotes"""
        self.tracker.add_packet(src_ip, timestamp)
        rate = self.tracker.get_packet_rate(src_ip)
        
        if rate > Config.RATE_LIMIT:
            self.add_alert(
                "MEDIUM", "RATE_LIMIT_EXCEEDED", src_ip, packet[IP].dst,
                f"Packet rate: {rate}/s (limit: {Config.RATE_LIMIT}/s)"
            )
            self.firewall.block_ip(src_ip, f"Rate limit exceeded ({rate}/s)", 120)
    
    def detect_known_attacks(self, packet, src_ip: str, dst_ip: str):
        """Detecta assinaturas conhecidas de ataque"""
        if not packet.haslayer(Raw):
            return
        
        payload = packet[Raw].load
        payload_str = payload.decode('utf-8', errors='ignore').lower()
        
        # SQL Injection
        sql_patterns = ["' or '1'='1", "union select", "drop table", "insert into"]
        for pattern in sql_patterns:
            if pattern in payload_str:
                self.add_alert(
                    "CRITICAL", "SQL_INJECTION", src_ip, dst_ip,
                    f"SQL injection pattern detected: {pattern}"
                )
                self.firewall.block_ip(src_ip, "SQL injection attempt", 3600)
                break
        
        # XSS
        xss_patterns = ["<script>", "javascript:", "onload=", "alert("]
        for pattern in xss_patterns:
            if pattern in payload_str:
                self.add_alert(
                    "HIGH", "XSS_ATTEMPT", src_ip, dst_ip,
                    f"XSS pattern detected: {pattern}"
                )
                break
        
        # Command Injection
        cmd_patterns = ["; ls", "; cat", "| id", "&& whoami", "`ping"]
        for pattern in cmd_patterns:
            if pattern in payload_str:
                self.add_alert(
                    "CRITICAL", "COMMAND_INJECTION", src_ip, dst_ip,
                    f"Command injection detected: {pattern}"
                )
                self.firewall.block_ip(src_ip, "Command injection attempt", 3600)
                break
    
    def analyze_packet(self, packet, src_ip: str, dst_ip: str, timestamp: float):
        """Análise completa do pacote"""
        # Verificar se IP já está bloqueado
        blocked, reason = self.firewall.is_blocked(src_ip)
        if blocked:
            return False, reason
        
        # Detecção de ataques
        self.detect_syn_flood(packet, src_ip, timestamp)
        self.detect_port_scan(packet, src_ip, timestamp)
        self.detect_brute_force(packet, src_ip, dst_ip, timestamp)
        self.detect_rate_limit_exceeded(packet, src_ip, timestamp)
        self.detect_known_attacks(packet, src_ip, dst_ip)
        
        return True, "analyzed"


