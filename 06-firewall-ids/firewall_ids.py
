class PacketSniffer:
    """Captura e processa pacotes de rede em tempo real"""
    
    def __init__(self, firewall: FirewallEngine, ids: IDSEngine):
        self.firewall = firewall
        self.ids = ids
        self.running = False
        self.packet_count = 0
        self.dropped_count = 0
        self.thread = None
    
    def process_packet(self, packet):
        """Processa um pacote capturado"""
        self.packet_count += 1
        
        if not packet.haslayer(IP):
            return
        
        src_ip = packet[IP].src
        dst_ip = packet[IP].dst
        
        # Determinar porta de destino
        dst_port = None
        protocol = "OTHER"
        if packet.haslayer(TCP):
            dst_port = packet[TCP].dport
            protocol = "TCP"
        elif packet.haslayer(UDP):
            dst_port = packet[UDP].dport
            protocol = "UDP"
        elif packet.haslayer(ICMP):
            protocol = "ICMP"
        
        # Verificar firewall
        if dst_port:
            allowed, reason = self.firewall.check_packet(src_ip, dst_port, protocol)
            if not allowed:
                self.dropped_count += 1
                return
        
        # Análise do IDS
        timestamp = time.time()
        self.ids.analyze_packet(packet, src_ip, dst_ip, timestamp)
        
        # Mostrar estatísticas periodicamente
        if self.packet_count % 100 == 0:
            self.print_stats()
    
    def print_stats(self):
        """Mostra estatísticas do sniffer"""
        print(f"\r\033[90m[STATS] Packets: {self.packet_count} | Dropped: {self.dropped_count}\033[0m", end='')
    
    def start_sniffing(self, interface: str = None, filter_str: str = None):
        """Inicia captura de pacotes"""
        if not SCAPY_AVAILABLE:
            print("[ERROR] Scapy não disponível. Use -m sim para simulação.")
            return
        
        self.running = True
        iface = interface or Config.INTERFACE
        filter_str = filter_str or Config.FILTER
        
        print(f"\n[+] Iniciando sniffer na interface {iface}")
        print(f"[+] Filtro: {filter_str}")
        print("[+] Pressione Ctrl+C para parar\n")
        
        try:
            sniff(iface=iface, filter=filter_str, prn=self.process_packet, store=False)
        except KeyboardInterrupt:
            self.stop()
        except Exception as e:
            print(f"[ERROR] Erro no sniffer: {e}")
    
    def stop(self):
        self.running = False
        print(f"\n[+] Sniffer parado. Total pacotes: {self.packet_count}")
        self.print_stats()


