class BackupManager:
    """Gerencia operações de backup"""
    
    def __init__(self, config_path: str = "backup_configs.json"):
        self.config_path = config_path
        self.configs: List[BackupConfig] = []
        self.history: Dict[str, List[BackupResult]] = {}
        self.lock = threading.Lock()
        self.load_configs()
    
    def load_configs(self):
        """Carrega configurações salvas"""
        if os.path.exists(self.config_path):
            try:
                with open(self.config_path, 'r') as f:
                    data = json.load(f)
                    for cfg_data in data:
                        cfg_data['destination_type'] = DestinationType(cfg_data['destination_type'])
                        cfg_data['backup_type'] = BackupType(cfg_data['backup_type'])
                        cfg_data['compression'] = CompressionType(cfg_data['compression'])
                        self.configs.append(BackupConfig(**cfg_data))
            except Exception as e:
                print(f"[!] Erro ao carregar configurações: {e}")
    
    def save_configs(self):
        """Salva configurações"""
        with open(self.config_path, 'w') as f:
            data = []
            for cfg in self.configs:
                cfg_dict = cfg.__dict__.copy()
                cfg_dict['destination_type'] = cfg_dict['destination_type'].value
                cfg_dict['backup_type'] = cfg_dict['backup_type'].value
                cfg_dict['compression'] = cfg_dict['compression'].value
                data.append(cfg_dict)
            json.dump(data, f, indent=2, default=str)
    
    def add_config(self, config: BackupConfig):
        """Adiciona nova configuração"""
        self.configs.append(config)
        self.save_configs()
        print(f"[+] Configuração '{config.name}' adicionada")
    
    def remove_config(self, name: str):
        """Remove configuração"""
        self.configs = [c for c in self.configs if c.name != name]
        self.save_configs()
        print(f"[-] Configuração '{name}' removida")
    
    def get_config(self, name: str) -> Optional[BackupConfig]:
        """Obtém configuração pelo nome"""
        for cfg in self.configs:
            if cfg.name == name:
                return cfg
        return None
    
    def calculate_source_size(self, source_dirs: List[str], exclude_patterns: List[str]) -> Tuple[int, int]:
        """Calcula tamanho total e número de arquivos da fonte"""
        total_size = 0
        total_files = 0
        
        for source_dir in source_dirs:
            if not os.path.exists(source_dir):
                continue
            
            for root, dirs, files in os.walk(source_dir):
                # Filtrar exclusões
                for pattern in exclude_patterns:
                    dirs[:] = [d for d in dirs if pattern not in d]
                
                for file in files:
                    filepath = os.path.join(root, file)
                    
                    # Verificar exclusão
                    excluded = False
                    for pattern in exclude_patterns:
                        if pattern in filepath:
                            excluded = True
                            break
                    
                    if not excluded:
                        try:
                            total_size += os.path.getsize(filepath)
                            total_files += 1
                        except:
                            pass
        
        return total_size, total_files
    
    def compress_files(self, source_dirs: List[str], output_path: str, 
                       compression: CompressionType, exclude_patterns: List[str]) -> Tuple[bool, str, int]:
        """Comprime arquivos para backup"""
        try:
            temp_dir = tempfile.mkdtemp()
            temp_archive = os.path.join(temp_dir, "backup")
            
            if compression == CompressionType.ZIP:
                temp_archive += ".zip"
                with zipfile.ZipFile(temp_archive, 'w', zipfile.ZIP_DEFLATED) as zf:
                    for source_dir in source_dirs:
                        if os.path.exists(source_dir):
                            for root, dirs, files in os.walk(source_dir):
                                for pattern in exclude_patterns:
                                    dirs[:] = [d for d in dirs if pattern not in d]
                                
                                for file in files:
                                    filepath = os.path.join(root, file)
                                    excluded = any(p in filepath for p in exclude_patterns)
                                    if not excluded:
                                        arcname = os.path.relpath(filepath, os.path.dirname(source_dir))
                                        zf.write(filepath, arcname)
            
            elif compression == CompressionType.TAR_GZ:
                temp_archive += ".tar.gz"
                with tarfile.open(temp_archive, 'w:gz') as tf:
                    for source_dir in source_dirs:
                        if os.path.exists(source_dir):
                            tf.add(source_dir, arcname=os.path.basename(source_dir),
                                   filter=lambda x: None if any(p in x.name for p in exclude_patterns) else x)
            
            elif compression == CompressionType.TAR_BZ2:
                temp_archive += ".tar.bz2"
                with tarfile.open(temp_archive, 'w:bz2') as tf:
                    for source_dir in source_dirs:
                        if os.path.exists(source_dir):
                            tf.add(source_dir, arcname=os.path.basename(source_dir))
            
            else:
                # Sem compressão - criar um tar simples
                temp_archive += ".tar"
                with tarfile.open(temp_archive, 'w') as tf:
                    for source_dir in source_dirs:
                        if os.path.exists(source_dir):
                            tf.add(source_dir, arcname=os.path.basename(source_dir))
            
            # Obter tamanho
            size = os.path.getsize(temp_archive)
            
            # Mover para destino final
            shutil.move(temp_archive, output_path)
            shutil.rmtree(temp_dir)
            
            return True, output_path, size
        
        except Exception as e:
            return False, str(e), 0
    
    def upload_to_destination(self, local_path: str, config: BackupConfig) -> Tuple[bool, str]:
        """Faz upload do backup para o destino configurado"""
        
        if config.destination_type == DestinationType.LOCAL:
            # Cópia local
            dest_file = os.path.join(config.destination_path, os.path.basename(local_path))
            shutil.copy2(local_path, dest_file)
            return True, dest_file
        
        elif config.destination_type == DestinationType.S3:
            if not BOTO3_AVAILABLE:
                return False, "boto3 não instalado. Instale com: pip install boto3"
            
            try:
                session = boto3.Session(
                    aws_access_key_id=config.s3_access_key,
                    aws_secret_access_key=config.s3_secret_key,
                    region_name=config.s3_region
                )
                s3 = session.client('s3')
                
                key = f"backups/{os.path.basename(local_path)}"
                s3.upload_file(local_path, config.s3_bucket, key)
                
                return True, f"s3://{config.s3_bucket}/{key}"
            
            except Exception as e:
                return False, str(e)
        
        elif config.destination_type == DestinationType.SFTP:
            if not PARAMIKO_AVAILABLE:
                return False, "paramiko não instalado. Instale com: pip install paramiko"
            
            try:
                transport = paramiko.Transport((config.sftp_host, config.sftp_port))
                
                if config.sftp_key_path:
                    key = paramiko.RSAKey.from_private_key_file(config.sftp_key_path)
                    transport.connect(username=config.sftp_username, pkey=key)
                else:
                    transport.connect(username=config.sftp_username, password=config.sftp_password)
                
                sftp = paramiko.SFTPClient.from_transport(transport)
                
                remote_path = os.path.join(config.destination_path, os.path.basename(local_path))
                sftp.put(local_path, remote_path)
                
                sftp.close()
                transport.close()
                
                return True, f"sftp://{config.sftp_host}:{config.sftp_port}/{remote_path}"
            
            except Exception as e:
                return False, str(e)
        
        else:
            return False, f"Tipo de destino não suportado: {config.destination_type}"
    
    def cleanup_old_backups(self, config: BackupConfig):
        """Remove backups antigos baseado na política de retenção"""
        try:
            dest_path = config.destination_path
            
            if config.destination_type == DestinationType.LOCAL and os.path.exists(dest_path):
                # Listar backups
                backups = []
                for f in os.listdir(dest_path):
                    if f.startswith(config.name) and (f.endswith('.enc') or f.endswith('.tar.gz') or f.endswith('.zip')):
                        filepath = os.path.join(dest_path, f)
                        backups.append((os.path.getmtime(filepath), filepath))
                
                # Ordenar por data
                backups.sort(reverse=True)
                
                # Remover backups excedentes
                for i, (_, filepath) in enumerate(backups):
                    if i >= config.max_backups:
                        os.remove(filepath)
                        print(f"[+] Backup antigo removido: {filepath}")
                
                # Remover backups mais antigos que retention_days
                cutoff = time.time() - (config.retention_days * 86400)
                for mtime, filepath in backups:
                    if mtime < cutoff:
                        os.remove(filepath)
                        print(f"[+] Backup expirado removido: {filepath}")
        
        except Exception as e:
            print(f"[!] Erro ao limpar backups antigos: {e}")
    
