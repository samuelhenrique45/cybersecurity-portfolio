class CryptoUtils:
    """Utilitários de criptografia para backup seguro"""
    
    @staticmethod
    def derive_key(password: str, salt: bytes = None) -> Tuple[bytes, bytes]:
        """Deriva chave AES da senha usando PBKDF2"""
        if salt is None:
            salt = get_random_bytes(32)
        key = hashlib.pbkdf2_hmac('sha256', password.encode(), salt, 100000, 32)
        return key, salt
    
    @staticmethod
    def encrypt_file(input_path: str, output_path: str, password: str) -> Tuple[bool, str]:
        """Criptografa um arquivo com AES-256-GCM"""
        try:
            # Derivar chave
            key, salt = CryptoUtils.derive_key(password)
            
            # Gerar IV
            iv = get_random_bytes(12)
            
            # Ler dados
            with open(input_path, 'rb') as f:
                plaintext = f.read()
            
            # Criptografar
            cipher = AES.new(key, AES.MODE_GCM, nonce=iv)
            ciphertext, tag = cipher.encrypt_and_digest(plaintext)
            
            # Salvar: [salt(32)][iv(12)][tag(16)][ciphertext]
            with open(output_path, 'wb') as f:
                f.write(salt)
                f.write(iv)
                f.write(tag)
                f.write(ciphertext)
            
            return True, output_path
        
        except Exception as e:
            return False, str(e)
    
    @staticmethod
    def decrypt_file(input_path: str, output_path: str, password: str) -> Tuple[bool, str]:
        """Decifra um arquivo criptografado"""
        try:
            with open(input_path, 'rb') as f:
                salt = f.read(32)
                iv = f.read(12)
                tag = f.read(16)
                ciphertext = f.read()
            
            # Derivar chave
            key, _ = CryptoUtils.derive_key(password, salt)
            
            # Decifrar
            cipher = AES.new(key, AES.MODE_GCM, nonce=iv)
            plaintext = cipher.decrypt_and_verify(ciphertext, tag)
            
            with open(output_path, 'wb') as f:
                f.write(plaintext)
            
            return True, output_path
        
        except Exception as e:
            return False, str(e)
    
    @staticmethod
    def calculate_checksum(file_path: str, algorithm: str = 'sha256') -> str:
        """Calcula checksum de um arquivo"""
        hash_func = hashlib.new(algorithm)
        with open(file_path, 'rb') as f:
            for chunk in iter(lambda: f.read(65536), b''):
                hash_func.update(chunk)
        return hash_func.hexdigest()


