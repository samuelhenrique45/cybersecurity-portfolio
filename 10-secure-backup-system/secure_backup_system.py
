    def upload_to_destination(self, local_path: str, config: BackupConfig) -> Tuple[bool, str]:
        """Faz upload do backup para o destino configurado"""
        
        if config.destination_type == DestinationType.LOCAL:
            # Cópia local
            try:
                os.makedirs(config.destination_path, exist_ok=True)
                dest_file = os.path.join(config.destination_path, os.path.basename(local_path))
                
                # Se for o mesmo filesystem, usar move; caso contrário, copy
                if os.path.exists(dest_file):
                    os.remove(dest_file)
                
                shutil.copy2(local_path, dest_file)
                print(f"   📁 Cópia local: {dest_file}")
                return True, dest_file
            except Exception as e:
                return False, str(e)
        
        elif config.destination_type == DestinationType.S3:
            if not BOTO3_AVAILABLE:
                return False, "boto3 não instalado. Instale com: pip install boto3"
            
            try:
                # Criar sessão
                session = boto3.Session(
                    aws_access_key_id=config.s3_access_key,
                    aws_secret_access_key=config.s3_secret_key,
                    region_name=config.s3_region
                )
                s3 = session.client('s3')
                
                # Gerar chave S3 (organizar por data)
                date_prefix = datetime.now().strftime("%Y/%m/%d")
                key = f"backups/{config.name}/{date_prefix}/{os.path.basename(local_path)}"
                
                # Upload com progresso
                print(f"   ☁️  Enviando para S3: s3://{config.s3_bucket}/{key}")
                
                # Upload com callback de progresso
                file_size = os.path.getsize(local_path)
                uploaded = 0
                
                def progress_callback(bytes_transferred):
                    nonlocal uploaded
                    uploaded += bytes_transferred
                    percent = (uploaded / file_size) * 100
                    print(f"\r   Progresso: {percent:.1f}% ({uploaded/(1024**2):.1f} MB / {file_size/(1024**2):.1f} MB)", end='')
                
                s3.upload_file(
                    local_path, config.s3_bucket, key,
                    Callback=progress_callback if file_size > 5*1024*1024 else None
                )
                print()  # Nova linha após progresso
                
                # Gerar URL pré-assinada (válida por 7 dias)
                url = s3.generate_presigned_url(
                    'get_object',
                    Params={'Bucket': config.s3_bucket, 'Key': key},
                    ExpiresIn=604800
                )
                
                return True, f"s3://{config.s3_bucket}/{key}"
            
            except Exception as e:
                return False, f"S3 upload failed: {str(e)}"
        
        elif config.destination_type == DestinationType.SFTP:
            if not PARAMIKO_AVAILABLE:
                return False, "paramiko não instalado. Instale com: pip install paramiko"
            
            try:
                # Conectar SFTP
                transport = paramiko.Transport((config.sftp_host, config.sftp_port))
                
                if config.sftp_key_path and os.path.exists(config.sftp_key_path):
                    key = paramiko.RSAKey.from_private_key_file(config.sftp_key_path)
                    transport.connect(username=config.sftp_username, pkey=key)
                else:
                    transport.connect(username=config.sftp_username, password=config.sftp_password)
                
                sftp = paramiko.SFTPClient.from_transport(transport)
                
                # Criar diretórios se não existirem
                remote_path = config.destination_path.rstrip('/')
                remote_file = f"{remote_path}/{os.path.basename(local_path)}"
                
                # Criar estrutura de diretórios
                dirs = remote_path.split('/')
                current_path = ''
                for d in dirs:
                    if d:
                        current_path += '/' + d
                        try:
                            sftp.stat(current_path)
                        except FileNotFoundError:
                            sftp.mkdir(current_path)
                
                # Upload com progresso
                file_size = os.path.getsize(local_path)
                uploaded = 0
                
                def sftp_progress(transferred, total):
                    nonlocal uploaded
                    uploaded = transferred
                    percent = (transferred / total) * 100
                    print(f"\r   Progresso: {percent:.1f}% ({transferred/(1024**2):.1f} MB / {total/(1024**2):.1f} MB)", end='')
                
                sftp.put(local_path, remote_file, callback=sftp_progress if file_size > 5*1024*1024 else None)
                print()  # Nova linha
                
                sftp.close()
                transport.close()
                
                return True, f"sftp://{config.sftp_host}:{config.sftp_port}{remote_file}"
            
            except Exception as e:
                return False, f"SFTP upload failed: {str(e)}"
        
        elif config.destination_type == DestinationType.WEBDAV:
            # WebDAV usando requests
            try:
                import requests
                from requests.auth import HTTPBasicAuth
                
                webdav_url = config.destination_path.rstrip('/')
                file_name = os.path.basename(local_path)
                remote_url = f"{webdav_url}/{file_name}"
                
                with open(local_path, 'rb') as f:
                    response = requests.put(
                        remote_url,
                        data=f,
                        auth=HTTPBasicAuth(config.sftp_username or '', config.sftp_password or ''),
                        timeout=300
                    )
                
                if response.status_code in [200, 201, 204]:
                    return True, remote_url
                else:
                    return False, f"WebDAV error: HTTP {response.status_code}"
            
            except Exception as e:
                return False, f"WebDAV upload failed: {str(e)}"
        
        else:
            return False, f"Tipo de destino não suportado: {config.destination_type}"
    
    def cleanup_old_backups(self, config: BackupConfig):
        """Remove backups antigos baseado na política de retenção"""
        try:
            if config.destination_type == DestinationType.LOCAL and os.path.exists(config.destination_path):
                # Listar backups
                backups = []
                pattern = f"{config.name}_"
                
                for f in os.listdir(config.destination_path):
                    if f.startswith(pattern) and (f.endswith('.enc') or f.endswith('.tar.gz') or f.endswith('.zip') or f.endswith('.tar.bz2') or f.endswith('.tar')):
                        filepath = os.path.join(config.destination_path, f)
                        backups.append((os.path.getmtime(filepath), filepath, f))
                
                # Ordenar por data (mais recente primeiro)
                backups.sort(reverse=True)
                
                # Remover backups excedentes (mais antigos que max_backups)
                removed_count = 0
                for i, (_, filepath, filename) in enumerate(backups):
                    if i >= config.max_backups:
                        os.remove(filepath)
                        removed_count += 1
                        print(f"   🗑️ Removido (excedente): {filename}")
                
                # Remover backups mais antigos que retention_days
                cutoff_time = time.time() - (config.retention_days * 86400)
                for mtime, filepath, filename in backups:
                    if mtime < cutoff_time:
                        os.remove(filepath)
                        removed_count += 1
                        print(f"   🗑️ Removido (expirado): {filename}")
                
                if removed_count > 0:
                    print(f"   ✅ {removed_count} backup(s) removido(s)")
                else:
                    print(f"   ✅ Nenhum backup excedente ou expirado")
            
            elif config.destination_type == DestinationType.S3:
                # S3 cleanup - implementar se necessário
                print(f"   ℹ️ Limpeza S3 deve ser configurada com lifecycle rules")
            
            elif config.destination_type == DestinationType.SFTP:
                # SFTP cleanup - implementar se necessário
                print(f"   ℹ️ Limpeza SFTP deve ser gerenciada manualmente")
        
        except Exception as e:
            print(f"   ⚠️ Erro ao limpar backups antigos: {e}")
    
    def save_history(self):
        """Salva histórico de backups em arquivo JSON"""
        history_file = os.path.join(os.path.dirname(self.config_path) if self.config_path else '.', "backup_history.json")
        data = {}
        
        for name, backups in self.history.items():
            data[name] = []
            for b in backups:
                data[name].append({
                    'success': b.success,
                    'backup_name': b.backup_name,
                    'timestamp': b.timestamp.isoformat(),
                    'size_bytes': b.size_bytes,
                    'duration_seconds': b.duration_seconds,
                    'source_size': b.source_size,
                    'files_backed_up': b.files_backed_up,
                    'destination_path': b.destination_path,
                    'checksum': b.checksum,
                    'error_message': b.error_message,
                    'metadata': b.metadata
                })
        
        try:
            with open(history_file, 'w') as f:
                json.dump(data, f, indent=2)
            print(f"   📝 Histórico salvo: {history_file}")
        except Exception as e:
            print(f"   ⚠️ Erro ao salvar histórico: {e}")
    
    def load_history(self):
        """Carrega histórico de backups de arquivo JSON"""
        history_file = os.path.join(os.path.dirname(self.config_path) if self.config_path else '.', "backup_history.json")
        
        if os.path.exists(history_file):
            try:
                with open(history_file, 'r') as f:
                    data = json.load(f)
                    for name, backups in data.items():
                        self.history[name] = []
                        for b in backups:
                            self.history[name].append(BackupResult(
                                success=b['success'],
                                backup_name=b['backup_name'],
                                timestamp=datetime.fromisoformat(b['timestamp']),
                                size_bytes=b['size_bytes'],
                                duration_seconds=b['duration_seconds'],
                                source_size=b['source_size'],
                                files_backed_up=b['files_backed_up'],
                                destination_path=b['destination_path'],
                                checksum=b['checksum'],
                                error_message=b.get('error_message'),
                                metadata=b.get('metadata', {})
                            ))
                print(f"   📂 Histórico carregado: {len(self.history)} configurações")
            except Exception as e:
                print(f"   ⚠️ Erro ao carregar histórico: {e}")


