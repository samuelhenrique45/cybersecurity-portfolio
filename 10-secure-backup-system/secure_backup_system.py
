def generate_status_report(manager: BackupManager):
    """Gera relatório de status para monitoramento"""
    print("\n" + "="*70)
    print(f"📊 RELATÓRIO DE STATUS - {datetime.now().strftime('%d/%m/%Y %H:%M:%S')}")
    print("="*70)
    
    total_backups = len(manager.configs)
    healthy_count = 0
    warning_count = 0
    critical_count = 0
    
    for cfg in manager.configs:
        print(f"\n📁 {cfg.name}")
        print(f"   📂 Origem: {', '.join(cfg.source_dirs)}")
        print(f"   🎯 Destino: {cfg.destination_path}")
        
        if cfg.name in manager.history and manager.history[cfg.name]:
            backups = manager.history[cfg.name]
            successful = [b for b in backups if b.success]
            
            if successful:
                last = successful[-1]
                time_since = (datetime.now() - last.timestamp).total_seconds() / 3600
                size_gb = last.size_bytes / (1024**3)
                
                print(f"   ✅ Último backup bem-sucedido: {last.timestamp.strftime('%d/%m/%Y %H:%M:%S')}")
                print(f"   ⏱️ Há {time_since:.1f} horas")
                print(f"   📦 Tamanho: {size_gb:.2f} GB" if size_gb >= 1 else f"   📦 Tamanho: {last.size_bytes/(1024**2):.2f} MB")
                print(f"   ⏱️ Duração: {last.duration_seconds:.1f} segundos")
                
                # Verificar saúde
                if time_since > 48:
                    print(f"   ⚠️ ATENÇÃO: Último backup há mais de 48 horas!")
                    warning_count += 1
                else:
                    healthy_count += 1
                
                # Verificar integridade do último backup (apenas para destino local)
                if cfg.destination_type == DestinationType.LOCAL and cfg.verify_integrity:
                    dest_file = last.destination_path
                    if os.path.exists(dest_file):
                        current_checksum = CryptoUtils.calculate_checksum(dest_file)
                        if current_checksum == last.checksum:
                            print(f"   🔒 Integridade: OK")
                        else:
                            print(f"   ❌ INTEGRIDADE: CORROMPIDO!")
                            critical_count += 1
                    else:
                        print(f"   ❌ Arquivo de backup não encontrado!")
                        critical_count += 1
            else:
                print(f"   ❌ Nenhum backup bem-sucedido encontrado")
                critical_count += 1
        else:
            print(f"   ❌ Nenhum backup realizado")
            critical_count += 1
    
    # Resumo final
    print("\n" + "="*70)
    print("📊 RESUMO GERAL")
    print("="*70)
    print(f"   Total de configurações: {total_backups}")
    print(f"   🟢 Saudáveis: {healthy_count}")
    print(f"   🟡 Atenção: {warning_count}")
    print(f"   🔴 Crítico: {critical_count}")
    
    if critical_count > 0:
        print("\n⚠️ Ações recomendadas:")
        if critical_count == total_backups:
            print("   • Execute backups imediatamente para todas as configurações")
        else:
            print("   • Verifique as configurações com status crítico")
            print("   • Execute backups manuais para restaurar a saúde")
    
    print("="*70)


