class PNGSteganography:
    """Esconde e extrai dados em imagens PNG usando LSB"""
    
    # Magic bytes para identificar dados escondidos
    MAGIC_HEADER = b'STEGv1'
    HEADER_SIZE = 16  # Magic (6) + tamanho (4) + flags (2) + reservado (4)
    
    def __init__(self, password: str = None):
        self.password = password
    
    def hide_data(self, image_path: str, output_path: str, data: bytes) -> bool:
        """
        Esconde dados em imagem PNG
        Estrutura: [MAGIC_HEADER(16)][tamanho_dados_cifrados(4)][dados_cifrados]
        """
        # Preparar dados
        if self.password:
            ciphertext, iv, tag, salt = CryptoUtils.encrypt_data(data, self.password)
            payload = self.MAGIC_HEADER + iv + tag + salt + len(ciphertext).to_bytes(4, 'big') + ciphertext
        else:
            payload = self.MAGIC_HEADER + len(data).to_bytes(4, 'big') + data
        
        # Carregar imagem
        img = Image.open(image_path)
        if img.mode != 'RGB':
            img = img.convert('RGB')
        
        width, height = img.size
        pixels = np.array(img, dtype=np.uint8)
        
        # Calcular capacidade
        available_bits = width * height * 3  # 3 canais RGB
        required_bits = len(payload) * 8
        
        if required_bits > available_bits:
            raise ValueError(f"Arquivo muito grande! Capacidade: {available_bits//8} bytes, Necessário: {required_bits//8} bytes")
        
        # Esconder dados nos bits menos significativos
        bits = LSBEncoder.encode_bytes(payload, 1)
        flat_pixels = pixels.reshape(-1)
        
        for i, bit in enumerate(bits):
            if i >= len(flat_pixels):
                break
            # Modificar apenas o bit menos significativo
            flat_pixels[i] = (flat_pixels[i] & 0xFE) | bit
        
        # Salvar imagem resultante
        pixels = flat_pixels.reshape(height, width, 3)
        output_img = Image.fromarray(pixels, mode='RGB')
        output_img.save(output_path, 'PNG')
        
        print(f"[+] Dados escondidos em: {output_path}")
        print(f"[+] Tamanho dos dados: {len(data)} bytes")
        print(f"[+] Capacidade usada: {required_bits//8} / {available_bits//8} bytes")
        
        return True
    
    def extract_data(self, image_path: str) -> Optional[bytes]:
        """Extrai dados escondidos de imagem PNG"""
        img = Image.open(image_path)
        pixels = np.array(img, dtype=np.uint8).reshape(-1)
        
        # Extrair bits LSB
        bits = [pixel & 1 for pixel in pixels]
        
        # Converter bits para bytes
        raw_data = LSBEncoder.decode_bytes(bits, 1)
        
        # Procurar magic header
        magic_pos = raw_data.find(self.MAGIC_HEADER)
        if magic_pos == -1:
            raise ValueError("Nenhum dado esteganográfico encontrado!")
        
        # Extrair dados
        header_end = magic_pos + len(self.MAGIC_HEADER)
        
        if self.password:
            # Formato com senha: iv(12) + tag(16) + salt(16) + size(4) + ciphertext
            iv = raw_data[header_end:header_end+12]
            tag = raw_data[header_end+12:header_end+28]
            salt = raw_data[header_end+28:header_end+44]
            data_size = int.from_bytes(raw_data[header_end+44:header_end+48], 'big')
            ciphertext = raw_data[header_end+48:header_end+48+data_size]
            
            plaintext = CryptoUtils.decrypt_data(ciphertext, self.password, iv, tag, salt)
            return plaintext
        else:
            # Sem senha
            data_size = int.from_bytes(raw_data[header_end:header_end+4], 'big')
            extracted = raw_data[header_end+4:header_end+4+data_size]
            return extracted
    
    def hide_file(self, image_path: str, output_path: str, file_path: str) -> bool:
        """Esconde um arquivo dentro da imagem"""
        with open(file_path, 'rb') as f:
            data = f.read()
        filename = os.path.basename(file_path).encode()
        payload = len(filename).to_bytes(2, 'big') + filename + data
        return self.hide_data(image_path, output_path, payload)
    
    def extract_to_file(self, image_path: str, output_path: str) -> bool:
        """Extrai arquivo da imagem"""
        data = self.extract_data(image_path)
        if data:
            # Extrair nome do arquivo
            filename_len = int.from_bytes(data[:2], 'big')
            filename = data[2:2+filename_len].decode()
            file_data = data[2+filename_len:]
            
            full_output = os.path.join(output_path, filename) if os.path.isdir(output_path) else output_path
            with open(full_output, 'wb') as f:
                f.write(file_data)
            print(f"[+] Arquivo extraído: {full_output}")
            return True
        return False


