class CryptoUtils:
    """Utilitários criptográficos para proteção dos dados"""
    
    @staticmethod
    def derive_key(password: str, salt: bytes = None) -> Tuple[bytes, bytes]:
        """Deriva chave AES a partir da senha"""
        if salt is None:
            salt = os.urandom(16)
        key = hashlib.pbkdf2_hmac('sha256', password.encode(), salt, 100000, 32)
        return key, salt
    
    @staticmethod
    def encrypt_data(data: bytes, password: str) -> Tuple[bytes, bytes, bytes]:
        """Cifra dados com AES-256-GCM"""
        key, salt = CryptoUtils.derive_key(password)
        iv = os.urandom(12)
        cipher = AES.new(key, AES.MODE_GCM, nonce=iv)
        ciphertext, tag = cipher.encrypt_and_digest(data)
        return ciphertext, iv, tag, salt
    
    @staticmethod
    def decrypt_data(ciphertext: bytes, password: str, iv: bytes, tag: bytes, salt: bytes) -> bytes:
        """Decifra dados com AES-256-GCM"""
        key, _ = CryptoUtils.derive_key(password, salt)
        cipher = AES.new(key, AES.MODE_GCM, nonce=iv)
        plaintext = cipher.decrypt_and_verify(ciphertext, tag)
        return plaintext
    
    @staticmethod
    def hash_file(filepath: str) -> str:
        """Calcula hash SHA-256 do arquivo"""
        sha256 = hashlib.sha256()
        with open(filepath, 'rb') as f:
            for chunk in iter(lambda: f.read(65536), b''):
                sha256.update(chunk)
        return sha256.hexdigest()


