# LSB (LEAST SIGNIFICANT BIT) IMPLEMENTATION
# ============================================================================

class LSBEncoder:
    """Codificação e decodificação LSB para imagens"""
    
    @staticmethod
    def encode_bytes(data: bytes, bits_per_byte: int = 1) -> List[int]:
        """Converte bytes em lista de bits LSB"""
        bits = []
        for byte in data:
            for i in range(bits_per_byte):
                bits.append((byte >> (7 - i)) & 1)
        return bits
    
    @staticmethod
    def decode_bytes(bits: List[int], bits_per_byte: int = 1) -> bytes:
        """Converte bits LSB de volta para bytes"""
        data = bytearray()
        for i in range(0, len(bits), bits_per_byte):
            byte = 0
            for j in range(bits_per_byte):
                if i + j < len(bits):
                    byte = (byte << 1) | bits[i + j]
            data.append(byte)
        return bytes(data)
    
    @staticmethod
    def bits_available(width: int, height: int, channels: int, bits_per_pixel: int = 1) -> int:
        """Calcula capacidade máxima de bits na imagem"""
        total_pixels = width * height * channels
        return total_pixels // bits_per_pixel


