class ImageComparator:
    """Compara imagens original e modificada para análise esteganográfica"""
    
    @staticmethod
    def compare(image1_path: str, image2_path: str) -> Dict:
        """Compara duas imagens e retorna diferenças"""
        img1 = Image.open(image1_path).convert('RGB')
        img2 = Image.open(image2_path).convert('RGB')
        
        pixels1 = np.array(img1, dtype=np.int16)
        pixels2 = np.array(img2, dtype=np.int16)
        
        diff = np.abs(pixels1 - pixels2)
        
        stats = {
            'max_diff': int(np.max(diff)),
            'mean_diff': float(np.mean(diff)),
            'pixels_changed': int(np.sum(diff > 0)),
            'total_pixels': pixels1.size,
            'percentage_changed': 100 * np.sum(diff > 0) / pixels1.size
        }
        
        # Criar imagem de diferença
        diff_img = Image.fromarray((diff * 10).clip(0, 255).astype(np.uint8))
        
        return stats, diff_img


